class Auth extends Public_Controller {

	function __construct()
	{
		parent::__construct();
		
		if(! $this->config->item('filebased', 'ion_auth'))
			$this->load->database();

		$this->form_validation->set_error_delimiters($this->config->item('error_start_delimiter', 'ion_auth'), $this->config->item('error_end_delimiter', 'ion_auth'));

		$this->lang->load('users/auth');
		$this->load->helper('language');

		if(! is_writable(SITE_PATH.'db/users.json') || ! is_writable(SITE_PATH.'db/groups.json'))
			show_error('Files users.json and groups.json in folder '.SITE_PATH.'db/ must be writable.');
	}

	//redirect if needed, otherwise display the user list
	function index()
	{
		if (!$this->logged_in())
			redirect('users/auth/login');
		else
			redirect('panel/users');
	}

	//log the user in
	function login()
	{
		if($this->logged_in()) redirect('panel');

		$this->data['title'] = "Login";

		//validate form input
		$this->form_validation->set_rules('identity', 'Identity', 'required');
		$this->form_validation->set_rules('password', 'Password', 'required');

		if ($this->form_validation->run() == true)
		{
			//check to see if the user is logging in
			//check for "remember me"
			$remember = (bool) $this->input->post('remember');

			if ($this->ion_auth->login($this->input->post('identity'), $this->input->post('password'), $remember))
			{
				//if the login is successful
				//redirect them back to the home page
				$this->session->set_flashdata('message', $this->ion_auth->messages());
				redirect('panel', 'refresh');
			}
			else
			{
				//if the login was un-successful
				//redirect them back to the login page
				$this->session->set_flashdata('message', $this->ion_auth->errors());
				redirect('users/login', 'refresh'); //use redirects instead of loading views for compatibility with MY_Controller libraries
			}
		}
		else
		{
			//the user is not logging in so display the login page
			//set the flash data error message if there is one
			$this->data['message'] = (validation_errors()) ? validation_errors() : $this->session->flashdata('message');

			$this->data['identity'] = array('name' => 'identity',
				'id' => 'identity',
				'type' => 'text',
				'value' => $this->form_validation->set_value('identity'),
				'class' => "form-control",
				'placeholder' => "Email",
				'autocomplete' => "off"
			);
			$this->data['password'] = array('name' => 'password',
				'id' => 'password',
				'type' => 'password',
				'class' => "form-control",
				'placeholder' => "Password",
				'autocomplete' => "off"
			);

			$this->template->view('login', $this->data);
		}
	}

